public class Card
{
   int suit = 0;
   int rank = 0;

   /* 
    * Purpose: To set the suit and rank of a card
    * Input: The suit and rank
    * Return: Nothing
    */
    public Card(int suit, int rank)
    {
        this.suit = suit;
        this.rank = rank;
    }
}


