public class Card
{
   char suit = '\0';
   char rank = '\0';

   /* 
    * Purpose: To set the suit and rank of a card
    * Input: The suit and rank
    * Return: Nothing
    */
    public Card(char suit, char rank)
    {
        this.suit = suit;
        this.rank = rank;
    }
}


